// 업장 이름으로 썸네일 이미지 경로 생성
export function getFactoryThumbnailImage(factoryName: string): string {
  const folderName = getFactoryImageFolder(factoryName);
  
  if (!folderName) {
    // 기본 이미지 반환
    return '/logo_donggori.png';
  }

  // 압축된 이미지가 있는지 먼저 확인
  const compressedImagePath = `/compressed_images/${folderName}.jpg`;
  
  // 압축된 이미지가 있으면 사용, 없으면 기존 방식 사용
  const firstImage = firstImages[folderName];
  if (firstImage) {
    return `/동고리_사진데이터/${folderName}/${firstImage}`;
  }

  // 폴백: 기본 이미지
  return '/logo_donggori.png';
}

