// 업장 이름으로 이미지 폴더 찾기
export function getFactoryImageFolder(factoryName: string): string | null {
  // 정확한 매칭 시도
  if (factoryImageMapping[factoryName]) {
    return factoryImageMapping[factoryName];
  }

  // 부분 매칭 시도 (업장 이름에 폴더 이름이 포함되어 있는 경우)
  for (const [folderName] of Object.entries(factoryImageMapping)) {
    if (factoryName.includes(folderName) || folderName.includes(factoryName)) {
      return folderName;
    }
  }

  return null;
}

